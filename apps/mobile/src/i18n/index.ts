type Locale = 'ja-JP' | 'en-US';

export const SUPPORTED_LOCALES: readonly Locale[] = ['ja-JP', 'en-US'] as const;

function resolveSupportedLocale(locale: string | null | undefined): Locale {
  if (typeof locale === 'string' && locale.toLowerCase().startsWith('ja')) {
    return 'ja-JP';
  }
  return 'en-US';
}

function getDeviceLocaleTag(): string | null {
  try {
    const resolved = Intl.DateTimeFormat().resolvedOptions().locale;
    return typeof resolved === 'string' ? resolved : null;
  } catch {
    return null;
  }
}

export function getDeviceLocale(): Locale {
  return resolveSupportedLocale(getDeviceLocaleTag());
}

